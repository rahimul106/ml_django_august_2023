#If you have the list numbers = [5, 2, 8, 2, 1], what method can you use to count the number of occurrences of the value 2?
#I use count method
#Here the given list 2 have total two occurrences
#Let's use count method and see output

#Given List
numbers = [5, 2, 8, 2, 1]

#Use count method find number of occurrences
occurrence = numbers.count(2)
print(occurrence)
#Output is 2 , so I succsseed to count the number of occurrences 2 in this list using count method